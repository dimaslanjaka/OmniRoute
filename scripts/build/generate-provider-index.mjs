#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "../..");
const registryRoot = path.join(projectRoot, "open-sse/config/providers/registry");
const outputPath = path.join(projectRoot, "open-sse/config/providers/__generated-index.ts");

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function parseProviderFilter(raw = process.env.NEXT_PUBLIC_ENABLED_PROVIDERS || "") {
  const enabled = new Set();
  const patterns = [];

  for (const part of raw.split(",")) {
    const value = part.trim();
    if (!value) continue;
    if (value.includes("*")) {
      patterns.push(new RegExp(`^${value.split("*").map(escapeRegExp).join(".*")}$`));
    } else {
      enabled.add(value);
    }
  }

  return { enabled, patterns };
}

function shouldRegisterProvider(id, filter) {
  if (filter.enabled.size === 0 && filter.patterns.length === 0) return true;
  if (filter.enabled.has(id)) return true;
  return filter.patterns.some((pattern) => pattern.test(id));
}

function compareProviderEntries(a, b) {
  return a.id.localeCompare(b.id);
}

async function registryEntryFromIndex(indexPath, relativeDir) {
  const source = await fs.readFile(indexPath, "utf8");
  const symbol = source.match(/export\s+const\s+([A-Za-z0-9_]+Provider)\s*:/)?.[1];
  const id = source.match(/\bid:\s*["']([^"']+)["']/)?.[1];

  if (!symbol || !id) {
    throw new Error(`Unable to read provider symbol/id from ${indexPath}`);
  }

  return {
    id,
    symbol,
    importPath: `./registry/${relativeDir.replaceAll(path.sep, "/")}/index.ts`,
  };
}

async function findRegistryEntries(dir = registryRoot, prefix = "") {
  const entries = [];

  for (const entry of await fs.readdir(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;

    const childRelativeDir = prefix ? path.join(prefix, entry.name) : entry.name;
    const childDir = path.join(dir, entry.name);
    const indexPath = path.join(childDir, "index.ts");

    try {
      await fs.access(indexPath);
      entries.push(await registryEntryFromIndex(indexPath, childRelativeDir));
    } catch (error) {
      if (error?.code !== "ENOENT") throw error;
      // Nested grouping directory only.
    }

    entries.push(...(await findRegistryEntries(childDir, childRelativeDir)));
  }

  return entries;
}

export async function generateProviderIndex() {
  const filter = parseProviderFilter();
  const entries = (await findRegistryEntries())
    .filter((entry) => shouldRegisterProvider(entry.id, filter))
    .sort(compareProviderEntries);

  const importLines = entries.map(
    (entry) => `import { ${entry.symbol} } from "${entry.importPath}";`
  );
  const registryLines = entries.map((entry) => `  ${JSON.stringify(entry.id)}: ${entry.symbol},`);

  const content = `${[
    "// Auto-generated by scripts/build/generate-provider-index.mjs.",
    "// Do not edit by hand; set NEXT_PUBLIC_ENABLED_PROVIDERS and rerun the generator.",
    "",
    'import type { RegistryEntry } from "./shared.ts";',
    "",
    ...importLines,
    "",
    "export const REGISTRY: Record<string, RegistryEntry> = {",
    ...registryLines,
    "};",
    "",
  ].join("\n")}`;

  await fs.writeFile(outputPath, content, "utf8");
  console.log(
    `[provider-index] registered ${entries.length} provider modules${
      process.env.NEXT_PUBLIC_ENABLED_PROVIDERS
        ? ` from NEXT_PUBLIC_ENABLED_PROVIDERS=${process.env.NEXT_PUBLIC_ENABLED_PROVIDERS}`
        : ""
    }`
  );
}

const isCli = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;

if (isCli) {
  generateProviderIndex().catch((error) => {
    console.error("[provider-index] failed to generate provider index", error);
    process.exit(1);
  });
}
